#include "ft_printf.h"

int	ft_numlen(long n)
{
	int	i;

	i = 0;
	if (n < 0)
		i++;
	while (n > 9 || n < -9)
	{
		n = n / 10;
		i++;
	}
	i++;
	return (i);
}
